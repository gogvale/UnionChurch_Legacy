		<h2>Pastors of The Union Church of Monterrey</h2>
		<p><strong>Richard Drew Walcott</strong> (1946-1956 and 1966-1983) &ndash; Ordained Methodist Minister &ndash; As the first pastor of the church, Drew Wolcott set the course for a vibrant congregation that would meet the needs of English-speaking residents of Monterrey for generations to come.  His vision for the constitution and bye-laws, for a building, for youth organizations, and for service to the community at large set a high standard for all who would follow him as pastor.  His work was supported by his loving wife Corrine.  They are lovingly remembered by both present and former members.</p>
		<p><strong>Dwayne L. Proett</strong> (1956-1959) &ndash; Ordained Presbyterian Minister &ndash; Rev. Proett demonstrated strong skills in counseling which was a mark of his ministry in the church.  He was actively involved with the church family and the community at large.  In his last sermon at the church, he related that the legacy he wished to be noted between &ldquo;was born&rdquo; and &ldquo;died&rdquo; was that &ldquo;he lived.&rdquo;</p>
		<p><strong>Laurence P. Byers</strong> (1959-1961) &ndash; Ordained Presbyterian Minister &ndash; Born in South China, Rev. Byers brought an international vision to his ministry in the church.  He was amazed how people of so many nationalities and worship traditions could come together in worship and work.  He had a strong focus on ecumenical work.</p>
		<p><strong>Charles R. McKean</strong> (1961-1962 and 1965-1966) &ndash; Rev. McKean came out of retirement to be interim pastor on two different occasions.  He was the former pastor of the Union Church in Mexico City.  His strong leadership in times of transition was helpful to the congregation.</p>
		<p><strong>Salle T. Lilly</strong> (1962-1965) &ndash; Ordained Methodist Minister &ndash; Of his time as pastor of the church, he said that through this church, he discovered how great God&rsquo;s love really is.</p>
		<p><strong>Robert W. Wolcott</strong>  (1979-1988) &ndash; Ordained Free Methodist Minister &ndash; Son of the first pastor and formerly a missionary to the Dominican Republic, Rev. Wolcott came to serve as co-pastor with his father and then as senior pastor upon his father&rsquo;s retirement.  He had a strong focus of training local pastors for ministry.  He was ably assisted by his wife Sylvia who dedicated her talents to developing a strong music ministry in support of his pastoral ministry.</p>
		<p><strong>Les Connell</strong>  (1988-1996) &ndash; Ordained Methodist Minister &ndash; Rev. Connell encouraged both mid-week activities at the church as well as home cell groups for Bible study.  He is remembered for his musical talents and using his guitar in worship.  His wife Marsha was instrumental in organizing the Ecumenical Women&rsquo;s Retreat.</p>
		<p><strong>Allen Owens</strong>  (1997-1998) &ndash; Ordained Baptist Minister &ndash; Rev. Owens was a gifted preacher.  During his short ministry in the church, there was much numerical growth as people came to saving faith in Jesus Christ.</p>
		<p><strong>Austin Chamberlain</strong>  )1998-2000) &ndash; A missionary with YWAM, Mr. Chamberlain served as interim pastor.  His ministry gave stability to the church in a time of transition and as the parsonage was being constructed.</p>
		<p><strong>David C. Long</strong> (2000-2012) &ndash; Ordained Baptist Minister &ndash; Rev. Long brings to the church many years of international experience as a pastor, Christian educator, and seminary professor.  He encourages the membership to creatively use their gifts in ministry.  He is ably assisted by his wife Sue who coordinates the music ministry of the church.  Using their gifts of service and hospitality, they minister to the congregation frequently with fellowship at their table.</p>
