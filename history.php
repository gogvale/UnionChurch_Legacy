		<p><strong>It is not known</strong> when the English-speaking Christians in Monterrey began to assemble for worship, Bible study and prayer in their native tongue. It is known that as early as the 1930s, in the basement of La Trinidad Methodist Church on the corner of Washington and Escobedo Streets, a group of 75-80 persons met regularly for a Sunday School class in English. On occasion, visiting ministers came and held English services for the group.</p>
		<p> <strong>In the spring of 1945</strong> one such visitor, Methodist Bishop Ivan Lee Holt, a church leader with a keen interest in Union Churches all over the world, shared a series of messages which the Holy Spirit used to encourage the English-speaking community in Monterrey. They had been praying and dreaming for some time about the formation of an official church body. As a consequence, on March 29 of the same year, some men from this group met at the Ancira Hotel to discuss the possibility of organizing a church. Those present were Dr. H.D. White, E.H. Weil, T.G. Gehring, Ramon Noble, Octavio Casavantes, Walter Thieme, M.E. Smith and Bishop Holt. Plans soon became a reality. On July 1, 1945, Dr. Warren Johnson, Pastor of the First Methodist Church of Fort Worth, Texas, following his sermon, presided the organizational meeting of The Union Church of Monterrey. In this meeting, the first members were received. Visiting ministers and local leaders shepherded the young congregation during its early days. On July 28, 1945, R. Drew Wolcott became the first pastor. Rev. Wolcott served the church in a two-part ministry for a total of 27 years. He is lovingly remembered for his pastoral care. Under his leadership, the church moved from La Trinidad Church to the Seventh Day Adventist Church on the corner of Vallarta and Matamoros Streets, using those facilities until construction and occupation of the present building in 1953. </p>
		<p>These words of Rev. Drew Wolcott on the 30 Anniversary of the dedication of the present sanctuary are still descriptive of our church: </p>
