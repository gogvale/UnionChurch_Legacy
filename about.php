  <h2>
    Local Information
  </h2>
  <p>
    <span style="font-weight: bold;">Monterrey is located about 150 miles</span> from the border cities of McAllen and Laredo. It is the third largest city in Mexico with a population of almost five million. Situated in a valley surrounded by the Sierra Madre Mountains with lush farmlands and orchards to the east and desert to the west, it is a major industrial center. English is readily spoken by many of the native residents, especially the younger generations. At an altitude of approximately 1700 feet, the summer temperatures vary between 85 and 95 degrees Fahrenheit, sometimes reaching 100+. Light freezes can occur in the winter, with December to February being the coldest months.
  </p>
