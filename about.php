  <h2>
    Leadership
  </h2>
  <p>
    <span style="font-weight: bold;">The leadership of the church</span> consists of the pastor, elders, deacons, stewards, and other organization and department leaders. These elected leaders form the Church Administrative Board. The Administrative Board is the governing authority and meets monthly to conduct the business of the church. The church has one part-time and two full-time employees other than the pastor: the secretary and two custodians.
  </p>
  <p>
    <span style="font-weight: bold;">Elders</span> are chosen for their exemplary Christian character. They are responsible for the spiritual life of the church. They advise and assist the pastor in spiritual matters.
  </p>
  <p>
    <span style="font-weight: bold;">Deacons</span> are responsible for the fellowship of the members, care of the sick, visitation of members and prospective members, the charitable and missionary outreach of the church and the use of benevolent funds.
  </p>
  <p>
    <span style="font-weight: bold;">Stewards</span> are responsible for the finances of the church, maintenance of church property, and policies with regard to employees other than the pastor.
  </p>
